package com.lxisoft.universitymanagement.person;

public class Person
{
	private String id;
	private String name;
	
	public String getID()
	{
		return id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setID(String id)
	{
		this.id=id;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
}