package com.lxisoft.metro.station.train.coach;

public class Coach
{
  
   private String name;
   private int fare;
  
   public void setName(String name)
   {
	   this.name=name;
   }
  
    public void setFare(int fare)
   {
	   this.fare=fare;
   }
   public String getName()
   {
	   return name;
   }
   
   public int getFare()
   {
	   return fare;
   }
   

}

